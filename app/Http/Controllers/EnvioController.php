     */
    public function store(Request $request)
    {
        //
        $envios= new Envio;
        $envios->fecha=$request->fecha;
        $envios->cliente_id=$request->cliente_id;
        $envios->producto_id=$request->producto_id;
        $envios->save();
        return Redirect::to('envios');
    }

